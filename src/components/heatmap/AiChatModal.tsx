// ─── Profile ──────────────────────────────────────────────────────────────────

type PicklePeteProfile = Record<string, string | number | boolean>;

const PROFILE_KEY = "pickle_pete_profile";

function loadProfile(): PicklePeteProfile | null {
  if (typeof window === "undefined") return null;
  try {
    const raw = localStorage.getItem(PROFILE_KEY);
    return raw ? (JSON.parse(raw) as PicklePeteProfile) : null;
  } catch {
    return null;
  }
}

function mergeAndSaveProfile(updates: Record<string, unknown>): PicklePeteProfile {
  const existing = loadProfile() ?? {};
  const merged: PicklePeteProfile = {
    ...existing,
    ...(updates as PicklePeteProfile),
    updatedAt: Date.now(),
  };
  try {
    localStorage.setItem(PROFILE_KEY, JSON.stringify(merged));
  } catch { /* quota — ignore */ }
  return merged;
}

function forgetProfile(): void {
  try {
    localStorage.removeItem(PROFILE_KEY);
  } catch { /* ignore */ }
}

function detectLanguage(text: string): "vi" | "en" {
  const vi = /[àáạảãâầấậẩẫăằắặẳẵèéẹẻẽêềếệểễìíịỉĩòóọỏõôồốộổỗơờớợởỡùúụủũưừứựửữỳýỵỷỹđ]/i;
  return vi.test(text) ? "vi" : "en";
}

function buildProfileSummary(profile: PicklePeteProfile): string {
  const parts: string[] = [];
  if (profile.dupr) parts.push(`${profile.dupr} DUPR`);
  if (profile.district) parts.push(String(profile.district));
  if (profile.play_time) parts.push(`${profile.play_time} player`);
  if (profile.play_style) parts.push(String(profile.play_style));
  return parts.slice(0, 4).join(" · ");
}

